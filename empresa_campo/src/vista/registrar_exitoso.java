public void limpiarTabla(){
        while(jTable1.getRowCount()!=0){
            ((DefaultTableModel)jTable1.getModel()).removeRow(0);
        }
    }

public void llenarTablaLotes(int id){
        List<Lote> lotes = oper.lotesVer(id);
        String aux="";
        if(lotes.size()>0){
            Iterator consulta = lotes.iterator();
            
            while(consulta.hasNext()){
                DefaultTableModel tabla = (DefaultTableModel)jTable1.getModel();
                Vector datos = new Vector();
                Lote fila = (Lote)consulta.next();
                aux = oper.obtener_nombre_suelo(fila.getIdTiposuelo());
                datos.add(fila.getNumeroLote());
                datos.add(fila.getSuperficieLote());
                datos.add(aux);
                tabla.addRow(datos);
                
            }
        }else{
            JOptionPane.showMessageDialog(null, "No hay registros");
        }
    }




